export const mutations = {
  addSelectedFlag(state, {rowId, seatId}) {
    const row = state.hall.seats.find(r => r.rowId === rowId);
    const seat = row.rowSeats.find(s => s.seatId === seatId);
    seat.selected = true;
    // console.log(state.hall);
    // console.log(seatId)
  },
  removeSelectedFlag(state, {rowId, seatId}) {
    const row = state.hall.seats.find(r => r.rowId === rowId);
    const seat = row.rowSeats.find(s => s.seatId === seatId);
    seat.selected = false;
    // console.log(state.hall);
    // console.log(seatId)
  },
}

